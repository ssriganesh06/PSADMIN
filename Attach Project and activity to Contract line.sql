SELECT B.PRODUCT_ID, C.CONTRACT_NUM, C.CONTRACT_LINE_NUM FROM PS_PROD_ITEM A,PS_OPT_PR_CREF_MST B, PS_CA_DETAIL C 
WHERE A.SETID=B.SETID 
AND A.PRODUCT_ID=B.PRODUCT_ID 
AND A.SETID= 'OPTUM'
AND A.PRODUCT_ID = C.PRODUCT_ID
AND B.REFERENCE_NUM IN (
'7712-773-001','8212-924-003','3012-974-007','8112-532206-006','29','7612-773-008','7812-663-005','8112-511002-002','7012-513000-002',
