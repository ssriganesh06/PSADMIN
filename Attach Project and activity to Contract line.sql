  SELECT A.CONTRACT_NUM,
         B.CONTRACT_LINE_NUM,
         D.BUSINESS_UNIT,
         D.PROJECT_ID,
         E.ACTIVITY_ID,
         0,
         'ONE',
         ' ',
         ' ',
         ' ',
         'N',
         ' '
    FROM PS_CA_CONTR_HDR A,
         PS_CA_DETAIL B,
         PS_OPT_CA_ADLN_TBL C,
         PS_PROJECT D,
         PS_PROJ_ACTIVITY E
   WHERE     A.CONTRACT_NUM = B.CONTRACT_NUM
         AND C.CONTRACT_NUM = B.CONTRACT_NUM
         AND C.CONTRACT_LINE_NUM = B.CONTRACT_LINE_NUM
         AND C.OPTCA_PROPOSALID = D.PROJECT_USER4
         AND D.BUSINESS_UNIT = E.BUSINESS_UNIT
         AND D.PROJECT_ID = E.PROJECT_ID
         AND B.PRICING_STRUCTURE = 'RATE'
         AND D.PROJECT_USER4 <> ' '
         AND EXISTS
                    (SELECT 'X'
                       FROM PS_CA_DETAIL_PROJ F
                      WHERE     F.PROJECT_ID = E.PROJECT_ID
                            AND F.ACTIVITY_ID = E.ACTIVITY_ID)
         AND EXISTS
                (SELECT 'X'
                   FROM PS_OPT_PRJ_ADDINFO I
                  WHERE     I.BUSINESS_UNIT = D.BUSINESS_UNIT 
                        AND I.PROJECT_ID = D.PROJECT_ID
                        AND I.PRODUCT_ID = B.PRODUCT_ID)
ORDER BY D.PROJECT_ID, E.ACTIVITY_ID, B.CONTRACT_LINE_NUM ;
